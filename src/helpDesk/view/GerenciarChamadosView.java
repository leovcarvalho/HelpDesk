    public void readJTable() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) jTableChamado.getModel();
        modelo.setNumRows(0);
        ExibirChamados chamado = new ExibirChamados();

        for(Chamado c : chamado.read()){
            modelo.addRow(new Object[]{
                c.getPatId(),
                c.getFunId(),
                c.getTexto(),
                c.getData()
            });
        }
    }

