package helpDesk.model;

public class Patrimonio {
    
    private int pat_id;
    private String pat_nome;
    private double pat_preco;
    private int fun_id;
    
    public int getPat_id() {
        return pat_id;
    }

    public void setPat_id(int pat_id) {
        this.pat_id = pat_id;
    }

    public String getPat_nome() {
        return pat_nome;
    }

    public void setPat_nome(String pat_nome) {
        this.pat_nome = pat_nome;
    }

    public double getPat_preco() {
        return pat_preco;
    }

    public void setPat_preco(double pat_preco) {
        this.pat_preco = pat_preco;
    }

    public int getFun_id() {
        return fun_id;
    }

    public void setFun_id(int fun_id) {
        this.fun_id = fun_id;
    }
    
    
    
}
